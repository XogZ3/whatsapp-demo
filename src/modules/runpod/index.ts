export async function generateImagesUploadToFirebaseGetURL(
  improvedPromptFromGroq: string,
  clientid: string,
): Promise<string[]> {
  try {
    const clientLoraInfo = await getUserLoraDetails(clientid);
    const lora_url = clientLoraInfo.loraURL;
    const lora_filename = clientLoraInfo.loraFilename;

    console.log('[+] got file name: ', lora_filename);

    // const improvedPromptFromGroq = await getImprovedPromptFromGroq(prompt);
    // console.log('[+] got improved prompt: ', improvedPromptFromGroq);

    const response = await fetch(runpodURL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${runpodBearerToken}`,
      },
      body: JSON.stringify({
        input: {
          num_images: 1,
          api_key: runpodApiKey,
          prompt: improvedPromptFromGroq,
          lora_url,
          lora_filename,
        },
      }),
    });

    console.log('[+] runpod res: ', JSON.stringify(response, null, 2));

    if (!response.ok) {
      throw new Error(`Runpod request failed with status: ${response.status}`);
    }

    const jobResponse = await response.json();

    const images = jobResponse?.output?.images || [];
    console.log(`got ${images.length} runpod images`);

    if (images.length === 0) {
      console.warn('No images were generated by Runpod.');
      return [];
    }

    const processPromises = images.map(async (image: string, index: number) => {
      const base64Content = image.split(';base64,').pop() || '';

      try {
        const filename = `${clientid || 'test'}_${uuidv4()}_${index + 1}.png`;
        const firebaseUrl = await uploadFileToFirebase(
          base64Content,
          clientid,
          filename,
        );
        return firebaseUrl;
      } catch (error) {
        console.error('Error processing image:', error);
        return null;
      }
    });

    const results = await Promise.all(processPromises);
    const firebaseURLs = results.filter((url): url is string => url !== null);

    return firebaseURLs;
  } catch (error) {
    console.error('Error generating images and uploading to Firebase:', error);
    throw new Error('Failed to generate and upload images');
  }
}
