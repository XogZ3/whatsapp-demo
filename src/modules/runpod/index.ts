const runpodURL = process.env.RUNPOD_URL as string;
const runpodBearerToken = process.env.RUNPOD_BEARER_TOKEN as string;
const runpodApiKey = process.env.RUNPOD_API_KEY as string;

export async function saveImageToDisk(base64Content: string, filename: string) {
  try {
    const fileContent = Buffer.from(base64Content, 'base64');
    const filePath = `${filename}.ignore.png`;
    fs.writeFileSync(filePath, fileContent);
  } catch (error) {
    console.error('Error saving image to disk:', error);
    throw new Error('Failed to save image to disk');
  }
}

export async function createTrainingJob(
  image_urls: string[],
  model_name: string,
) {
  try {
    const response = await fetch(
      'https://api.runpod.ai/v2/rob1itdpqaacn3/run',
      {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          Authorization: `Bearer ${runpodBearerToken}`,
        },
        body: JSON.stringify({
          input: {
            api_key: runpodApiKey,
            image_urls,
            model_name,
          },
        }),
      },
    );

    if (!response.ok) {
      throw new Error(`RunPod request failed with status: ${response.status}`);
    }

    const jobResponse = await response.json();
    return jobResponse.id;
  } catch (error) {
    console.error('Error creating training job:', error);
    throw new Error('Failed to create training job');
  }
}
